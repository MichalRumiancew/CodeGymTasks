package pl.codegym.task.task04.task0433;


/* 
Zobaczyć dolary w przyszłości
*/

public class Solution {
    public static void main(String[] args) throws Exception {

        int i = 0;

        while (i < 10) {
            int j = 0;
            while (j < 10) {
                System.out.print("$");
                j++;
            }
            System.out.println();
            i++;
        }

    }
}
